    <tr>
        <th scope="row">ADVERBS (ZARFLAR)</th>
    </tr>
    <tr>
        <td>İngilizce zarflar cümle içerisinde fiilden sonra gelirler ve sonrasında geldiği fiili nitelerler. Kullanım
            şekillerini aşağıda görebilirsiniz.
        </td>
    </tr>
    <tr>
        <td><b>1)</b> Genellikle sıfatların sonuna “ly” takısı eklenerek oluşurlar.<br><br>
            He had his dinner very <b>quickly.</b></td>
    </tr>
    <tr>
        <td><b>2)</b> Sıfat “y” ile bitiyorsa “y” düşer ve yerine “ily” takısı alır. <br><br>
            You can learn English very <b>easily</b> with us.
        </td>
    </tr>
    <tr>
        <td><b>3)</b> Sıfat “le” ile bitiyorsa “e” düşer ve yerine “y” gelir.<br><br>
            It was very terrible food. He cookied it very <b>terribly.</b></td>
    </tr>
    <tr>
        <td><b>4)</b> Aşağıdaki sıfatlar, zarf hallerinde herhangi bir değişikliğe uğramazlar.<br><br>
            fast<br>
            hard<br>
            early<br>
            late<br>
            high<br>
            straight<br></td>
    </tr>
    <tr>
        <td><b>5) </b>Aşağıdaki sıfat düzensizdir ve zarf halinde tamamen farklı bir kelime olur.<br><br>
            <b>Sıfat &nbsp;&nbsp;&nbsp;&nbsp; Zarf</b> <br>
            good &nbsp;&nbsp;&nbsp; well
        </td>

    </tr>

