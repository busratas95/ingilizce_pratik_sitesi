    </tr>
    <tr>
        <td>İngilizce sıfatlar (adjectives) cümle içerisinde nesneden önce gelerek nesnenin “nasıl” olduğunu
            tanımlarlar.
        </td>
    </tr>
    <tr>
        <td>They bought a very <b>nice</b> car.<br><br>
            nice: güzel (sıfat)<br>
            car: araba (nesne)<br>
            Nesneye “nasıl” sorusunu sorduğumuzda (nasıl araba?) aldığımız cevap bize sıfatın ne olduğunu gösterir.
        </td>
    </tr>
    <tr>
        <td>They live in an <b>expensive</b> house.<br><br>
            Tahmin edebileceğiniz gibi bu cümlede sıfat “expensive”dir çünkü “nasıl” sorusu sorulduğunda aldığımız cevap
            “pahalı” dır.
        </td>
    </tr>


